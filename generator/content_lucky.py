def build_number_content(n):
    """生成一个数字 00-99 的完整内容"""
    import random
    key = f"{n:02d}"
    tens = n // 10
    ones = n % 10
    d_ones = DIGIT_MEANING[ones]
    d_tens = DIGIT_MEANING[tens]

    # 核心含义
    if key in SPECIAL:
        core = SPECIAL[key]
    else:
        core = (f"{key} ဂဏန်းသည် {d_tens['theme']} ({d_tens['trait']}) နှင့် "
                f"{d_ones['theme']} ({d_ones['trait']}) တို့၏ ပေါင်းစပ်စွမ်းအားကို ကိုယ်စားပြုသည်။ "
                f"ဤနံပါတ်ကို ပိုင်ဆိုင်သူများသည် {d_ones['trait']} ကို အခြေခံ၍ "
                f"ဘဝတွင် အောင်မြင်မှုများ ရရှိနိုင်ကြသည်။")

    # 用数字做种子,让每个数字的运势/标签固定但不同
    rnd = random.Random(n)
    fortune = rnd.choice(FORTUNE_VARIANTS)
    tags = rnd.sample(TAG_POOL, 3)

    return {"key": key, "core": core, "fortune": fortune, "tags": tags}


